export function buildSpecPrompt(
  idea: string,
  analysis: string,
  projectType: string,
): string {
  const isTheme = projectType === "wordpress_theme";

  return `[BUILD_SPEC]
${WORDPRESS_PRODUCTION_SYSTEM_PROMPT}

Create a detailed project specification for a WordPress ${isTheme ? "theme" : "plugin"}.

USER IDEA: "${idea}"

ANALYSIS:
${analysis}

Return JSON matching this schema exactly:
{
  "projectType": "${projectType}",
  "architecture": "description of the architecture",
  "fileStructure": [
    { "filePath": "relative/path/file.php", "description": "what this file does" }
  ],
  "apiEndpoints": [],
  "buildScript": "",
  "testScript": "php -l *.php"
}

${isTheme ? THEME_FILE_STRUCTURE_GUIDE : PLUGIN_FILE_STRUCTURE_GUIDE}

RULES:
- fileStructure must include ALL files required for the project to work after installation
- Every filePath must be relative to the project root (no leading slash)
- Include inc/theme-data.php and inc/customizer.php for themes
- Include all class files under includes/, admin/, public/ for plugins
- Do NOT include binary files (png, jpg, gif, woff) — the agent handles those separately
- Minimum viable theme: style.css, functions.php, index.php, front-page.php, header.php, footer.php, inc/theme-data.php, inc/customizer.php
- Minimum viable plugin: {slug}.php, uninstall.php, readme.txt, includes/class-loader.php, includes/class-plugin.php, includes/class-activator.php, includes/class-deactivator.php`;
}

const THEME_FILE_STRUCTURE_GUIDE = `
REQUIRED THEME FILES (always include):
  style.css, functions.php, index.php, front-page.php, single.php,
  page.php, comments.php, header.php, footer.php, 404.php, archive.php,
  inc/theme-data.php, inc/customizer.php,
  assets/js/main.js, assets/css/ (component files)
  template-parts/ (section partials for front-page)

CRITICAL: style.css MUST include CSS for every template (front-page sections AND
inner-page templates: .single-post, .archive-content, .post-card, .page-header,
.page-content, .comments-area, .comment-form, .nav-links, .no-results).
Common mistake: generating CSS only for front-page hero/sections and leaving
single.php / archive.php / comments unstyled.`;

const PLUGIN_FILE_STRUCTURE_GUIDE = `
REQUIRED PLUGIN FILES (always include):
  {slug}.php (bootstrap), uninstall.php, readme.txt,
  includes/class-loader.php, includes/class-plugin.php,
  includes/class-activator.php, includes/class-deactivator.php,
  admin/class-{slug}-admin.php,
  public/class-{slug}-public.php`;

